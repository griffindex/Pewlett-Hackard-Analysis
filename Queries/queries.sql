-- Employee count by department number

SELECT COUNT(CE.EMP_NO),
	DE.DEPT_NO INTO RETIRING_DEPT
FROM CURRENT_EMP AS CE
LEFT JOIN DEPT_EMP AS DE ON CE.EMP_NO = DE.EMP_NO
GROUP BY DE.DEPT_NO
ORDER BY DE.DEPT_NO;

