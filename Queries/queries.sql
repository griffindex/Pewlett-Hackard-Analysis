SELECT RI.EMP_NO,
	RI.FIRST_NAME,
	RI.LAST_NAME,
	DE.TO_DATE INTO CURRENT_EMP
FROM RETIREMENT_INFO AS RI
LEFT JOIN DEPT_EMP AS DE ON RI.EMP_NO = DE.EMP_NO
WHERE DE.TO_DATE = ('9999-01-01');

